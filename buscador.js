// Unir todo en un solo array para el buscador
const todosLosVideos = [...videos, ...videosSubtitulados];

// Función para renderizar los resultados
function renderizarResultados(resultados) {
  const contenedor = document.getElementById('resultados');
  contenedor.innerHTML = ''; // Limpiar resultados anteriores

  if (resultados.length === 0) {
    contenedor.innerHTML = '<p>No se encontraron resultados.</p>';
    return;
  }

  resultados.forEach(video => {
    const tarjeta = document.createElement('div');
    tarjeta.classList.add('tarjeta-video');

    const titulo = document.createElement('h3');
    titulo.textContent = video.titulo;

    const iframe = document.createElement('div');
    iframe.innerHTML = video.iframe;

    const banner = document.createElement('div');
    banner.innerHTML = video.banner;

    tarjeta.appendChild(titulo);
    tarjeta.appendChild(iframe);
    tarjeta.appendChild(banner);

    contenedor.appendChild(tarjeta);
  });
}

// Función de búsqueda
function buscarVideos(termino) {
  const terminoMinuscula = termino.toLowerCase();
  const resultados = todosLosVideos.filter(video =>
    video.titulo.toLowerCase().includes(terminoMinuscula)
  );
  renderizarResultados(resultados);
}

// Evento para el input de búsqueda
document.addEventListener('DOMContentLoaded', () => {
  const inputBusqueda = document.getElementById('input-busqueda');
  inputBusqueda.addEventListener('input', (e) => {
    buscarVideos(e.target.value);
  });
});
